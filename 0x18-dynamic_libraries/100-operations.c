 /**
 * add - addition func
 * @a: number1
 * @b: number2
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub function
 * @a: number1
 * @b: number2
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - mul function
 * @a: number1
 * @b: number2
 *
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - div function
 * @a: number1
 * @b: number2
 *
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modules function
 * @a: number1
 * @b: number2
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
